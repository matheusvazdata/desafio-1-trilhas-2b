function somaDeDoisNumeros(numero1, numero2) { // Criação da função somaDeDoisNumeros com os argumentos numero1 e numero2
  let soma = numero1 + numero2; // Criação da variável soma que recebe a soma dos argumentos numero1 e numero2

  return soma; // Retorno da variável soma
}

let testeSoma = somaDeDoisNumeros(10, 20); // Chamada da função somaDeDoisNumeros com os argumentos 10 e 20 e atribuição do retorno à variável testeSoma

console.log(testeSoma); // Impressão da variável testeSoma